      SUBROUTINE COMPND (IZ,IN,ENERGY,MAXC)
C
C     ORIGINAL JULIAN SUBROUTINE
C
C     ***********
C     FORM J DISTRIBUTION OF INITIAL COMPOUND NUCLEI
      COMMON /XQSHL/ NOSHL, ALEV(100), AJCN(150)
      Z=IZ
      A=IZ+IN
      SR=MOD(IZ+IN,2)*.5
      CALL GCLDP (A,Z,0,FL,AL,SIG,F1,D,ACONST,SR,1.)
