C   GRAPHING
C
  160 CONTINUE
      WRITE(10, 250)
      WRITE(10, 260)(ANGLE1(M),M=1,18)
      WRITE(10, 270)
      WRITE(10, 280) (ANGLE2(M),M=1,18)
      WRITE(10, 290)
      IF (ELOW.LT.0.01) GO TO 170
      E2=ELOW
      WRITE(10, 300) E2,(NR(NP,1,L),L=1,18)
  170 DO 180 K=2,30
      AK=K
