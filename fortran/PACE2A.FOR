      SUBROUTINE TCCAL (JAH,JZ,JA,NE,KUC,JMT,BTT,SIV,BMV,BV,BW,BWV,SCX
     1 ,EP,FTHRS,FCUT,BSO,SSOT,BSOT,IRAD,JMESH,TP,LMX)
